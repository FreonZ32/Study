#include"Auout.h"

void Aout(int arr[][C], int R, int C)
{
	for (int i = 0; i < R; i++)
	{
		for (int j = 0; j < C; j++)
		{
			cout << arr[i][j] << "\t";
		}cout << endl;
	}cout << endl;
}
